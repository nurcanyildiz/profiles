const button = document.querySelector('#btn');
const input = document.querySelector('#input');
const content = document.querySelector('.content');

/*console.log(profile);*/
button.addEventListener('click', function() {
  let profile = null;
  // Try to find a profile in the profiles array
  for (let i = 0; i < profiles.length; i++) {
    if (profiles[i].name.toLowerCase() === input.value.toLowerCase()) {
      profile = profiles[i];
    }
  }
  // remove old content and input value
  content.innerHTML = '';

  // If a profile is found, do stuff
  if (profile !== null) {
    // Add name
    let nameHeader = document.createElement('div');
    nameHeader.classList.add('name-header');
    content.append(nameHeader);
    nameHeader.innerHTML = `${profile.name} `;

    let image = document.createElement('img');
    content.append(image);
    image.classList.add('img-style');
    image.src = `${profile.images}`;

    let about = document.createElement('div');
    about.classList.add('about');
    content.append(about);

    let occupation = document.createElement('div');
    occupation.classList.add('occupation', 'row');
    about.append(occupation);

    let occupationName = document.createElement('span');
    occupationName.innerHTML = 'Occupation:';
    occupationName.classList.add('type');
    occupation.append(occupationName);

    let occupationDetail = document.createElement('span');
    occupationDetail.innerHTML = profile.occupation;
    occupation.append(occupationDetail);
    about.append(occupation);

    let nationality = document.createElement('div');
    nationality.classList.add('nationality', 'row');
    about.append(nationality);

    let nationalityName = document.createElement('span');
    nationalityName.innerHTML = 'Nationality: ';
    nationalityName.classList.add('type');
    nationality.append(nationalityName);

    let nationalityDetail = document.createElement('span');
    nationalityDetail.innerHTML = profile.nationality;
    nationality.append(nationalityDetail);

    let born = document.createElement('div');
    born.classList.add('born', 'row');
    about.append(born);

    let bornDetail = document.createElement('span');
    bornDetail.innerHTML = 'Born: ';
    born.append(bornDetail);
    bornDetail.classList.add('type');

    let bornDate = document.createElement('span');
    bornDate.innerHTML = profile.born;
    born.append(bornDate);

    let borderBottom = document.createElement('hr');
    about.append(borderBottom);

    let titleOfLife = document.createElement('h4');
    content.append(titleOfLife);
    titleOfLife.innerHTML = 'LIFE';

    let life = document.createElement('div');
    life.classList.add('life');
    content.append(life);

    let aboutLifeDetail = document.createElement('span');
    life.append(aboutLifeDetail);

    // Gets all the text between 0 and 150 characters.
    let smallString = profile.life.slice(0, 100);

    aboutLifeDetail.innerHTML = smallString + ' ... ';

    let button = document.createElement('button');
    aboutLifeDetail.append(button);
    button.innerHTML = 'Read more';
    button.classList.add('button-more');

    button.addEventListener('click', function() {
      aboutLifeDetail.classList.toggle('show-all');

      if (aboutLifeDetail.classList.contains('show-all')) {
        aboutLifeDetail.innerHTML = profile.life;
        button.innerHTML = 'Read less';
        aboutLifeDetail.append(button);
      }

      if (!aboutLifeDetail.classList.contains('show-all')) {
        aboutLifeDetail.innerHTML = smallString + ' ...';
        button.innerHTML = 'Read more';
        aboutLifeDetail.append(button);
      }
    });
  } else {
    let error = document.createElement('p');
    error.innerHTML = input.value + ' not found.';
    content.append(error);
    error.classList.add('errorStyle');
  }

  // clear the input value after clicking the button
  input.value = '';
});


